class ComparisonOperators{
    public static void main(String args[]){
        int num1, num2;
        num1 = 3;
        num2 = 5;
        System.out.println("3 < 5 is" + (num1 < num2));
        System.out.println("3 > 5 is" + (num1 > num2));
        System.out.println("3 <= 5 is" + (num1 <= num2));
        System.out.println("3 >= 5 is " + (num1>= num2));
        System.out.println("3 != 5 is " + (num1 != num2));
        System.out.println("3 == 5 is " + (num1 == num2));
    }
}